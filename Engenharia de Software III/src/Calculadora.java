public class Calculadora {
    public double soma(double a, double b){
        return a+b;
    }
    public double diferenca(double a, double b){
        return a-b;
    }
    public double divisao(double a, double b){
        return a/b;
    }
    public double produto(double a, double b){
        return a*b;
    }



}
